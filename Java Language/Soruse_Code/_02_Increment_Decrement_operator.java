public class _02_Increment_Decrement_operator {
    public static void main(String[] args) {
        int num = 50;
        int value = 90;
        System.out.println(num++); //First Use it Then increment it
                System.out.println(num); 

        System.out.println(++num); //First incrment it then use
                System.out.println(num); 

        
        System.out.println(value--); //First Use it Then decrement it
                System.out.println(value); 


        System.out.println(--value); //First decrment it then use
                System.out.println(value); 


        char ch='A';
        System.out.println(++ch);
    
        // //Quick Quiz
        int y = 7;
        int x = ++y*8;

        System.out.println(x);




    }   

}
